/*
Задача 2: Работа с try/catch

Напишите функцию divideNumbers, которая принимает два числа и возвращает результат их деления. Если второе число — это 0, функция должна выбросить ошибку с сообщением "Нельзя делить на 0", а если оба аргумента не числа, то ошибка должна быть выброшена с сообщением "Ожидались числа". Используйте try/catch для обработки ошибок.
Требования:
Если оба аргумента являются числами и второе число не равно 0, функция должна вернуть результат деления.
Если второе число равно 0, выбрасывается ошибка "Нельзя делить на 0".
Если хотя бы один из аргументов не является числом, выбрасывается ошибка "Ожидались числа".
*/



function divideNumbers(a, b) {
    try {
        // проверка:
        if (typeof a !== 'number' || typeof b !== 'number') {
            throw new Error("Ожидались числа!");
        }
        
        if (b === 0) {
            throw new Error("Нельзя делить на 0!");
        }
        return a / b;

    } catch (error) {
        console.error(error.message);
    }
}

console.log(divideNumbers(17, 3)); 
console.log(divideNumbers(10, 0)); 
console.log(divideNumbers(4, 'х')); 


